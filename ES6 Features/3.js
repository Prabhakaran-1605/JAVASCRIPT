// forEach method  ---  [].forEach() --- Iterating the array and executes the provided function and returns undefined 


let ids = [100,101,103,103,104]

ids.forEach( (id)=> {console.log(id) })

// Here return is not mentioned hence iteration and execution of the function only happens 
// Output will be 
// 100
// 101
// 103
// 103
// 104